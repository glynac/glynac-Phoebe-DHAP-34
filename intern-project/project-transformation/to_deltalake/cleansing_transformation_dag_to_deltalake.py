def clean_lead_log(input_file, output_file):
    """
    Cleans the lead_log.csv file by dropping nulls and duplicates,
    and standardizing the date format.
    """
    if not os.path.exists(input_file):
        raise AirflowException(f"Input file not found: {input_file}")

    df = pd.read_csv(input_file)
    initial_rows = len(df)
    df.dropna(subset=['id', 'lead_id', 'created_at'], inplace=True)
    rows_after_na = len(df)
    df.drop_duplicates(inplace=True)
    rows_after_duplicates = len(df)
    df['created_at'] = pd.to_datetime(df['created_at'], utc=True)
    df.to_csv(output_file, index=False)

    na_dropped = initial_rows - rows_after_na
    duplicates_dropped = rows_after_na - rows_after_duplicates
    print(f"lead_log.csv: Dropped {na_dropped} (nulls), {duplicates_dropped} (duplicates).")


# A dispatch table that maps file names to their corresponding cleaning function.
CLEANING_FUNCTIONS = {
    'lead_log.csv': clean_lead_log,
    'paid_transactions.csv': clean_paid_transactions,
    'referral_rewards.csv': clean_referral_rewards,
    'user_logs.csv': clean_user_logs,
    'user_referral_logs.csv': clean_user_referral_logs,
    'user_referral_statuses.csv': clean_user_referral_statuses,
    'user_referrals.csv': clean_user_referrals,
}

# This block defines the DAG, its properties, and the tasks within it.
with DAG(
    dag_id='cleansing_and_transformation_dag',
    start_date=datetime(2025, 1, 1),
    schedule=None,
    catchup=False,
    tags=['transformation', 'cleansing', 'staging'],
) as dag:

    # This list will hold all the cleaning task objects for dependency setting.
    cleaning_tasks = []
    # This loop dynamically generates one cleaning task for each file.
    for file in CSV_FILES:
        task = PythonOperator(
            task_id=f'clean_{file.replace(".csv", "")}',
            python_callable=CLEANING_FUNCTIONS[file],
            op_kwargs={
                'input_file': os.path.join(RAW_DATA_PATH, file),
                'output_file': os.path.join(CLEANED_DATA_PATH, file.replace('.csv', '_cleaned.csv')),
            }
        )
        cleaning_tasks.append(task)

    # This task triggers the downstream validation and publish DAG.
    trigger_downstream_dag = TriggerDagRunOperator(
        task_id='trigger_output_validation_dag',
        trigger_dag_id='output_validation_and_publish_dag_to_deltalake',
        # Passes the logical date to the triggered DAG for consistency.
        conf={"logical_date": "{{ ds }}"},
    )

    # This sets the dependency, ensuring the trigger runs only after all cleaning tasks succeed.
    cleaning_tasks >> trigger_downstream_dag